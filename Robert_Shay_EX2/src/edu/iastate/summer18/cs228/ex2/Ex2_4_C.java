/**
 * Compares the execution time of two different loops and outputs it to the console.
 * @author Robert Shay
 *
 */
public class Ex2_4_C {
	public static void main(String[] args) {
		for(int i = 1; i <= 1000; i++) {
			long a = loopA(i);
			long b = loopB(i);
			
			System.out.print("\nN = " + i + " Loop A = " + a + " Loop B = " + b + " ");
			if(a < b)
				System.out.print(" A is faster.");
			else
				System.out.print( "B is faster.");
		}
	}

	public static long loopA(long n) {
		long start = System.nanoTime();
		long sum = 0;

		for (long i = 1; i <= n; i++)
			for (long j = 1; j <= 10000; j++)
				sum = sum + j;

		return System.nanoTime() - start;
	}

	public static long loopB(long n) {
		long start = System.nanoTime();
		long sum = 0;

		for (long i = 1; i <= n; i++)
			for (long j = 1; j <= n; j++)
				for (long k = 1; k <= n; k++)
					sum = sum + k;

		return System.nanoTime() - start;
	}
}

