/**
 * Compares the execution time of two different implementations of the toArray() method for ResizableArrayBag and prints it in the console.
 * @author Robert Shay
 *
 */
public class Ex2_4_D {
	public static void main(String[] args) {
		ResizableArrayBag<String> bag = new ResizableArrayBag<>();

		for (long i = 1; i <= 1000; i++) {
			for (long j = 0; j < i; j++) {
				bag.add("a");
			}
			long start = System.nanoTime();
			bag.toArray();
			long end = System.nanoTime();
			
			System.out.print("\nN = " + i + " takes " + (end - start) + " nanoseconds.");
			
			bag.clear();
		}
	}
}

